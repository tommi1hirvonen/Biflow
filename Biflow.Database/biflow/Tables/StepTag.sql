CREATE TABLE [biflow].[StepTag]
(
	[StepId] UNIQUEIDENTIFIER NOT NULL,
	[TagId] UNIQUEIDENTIFIER NOT NULL,
	CONSTRAINT [PK_StepTag] PRIMARY KEY CLUSTERED ([StepId], [TagId]),
	CONSTRAINT [FK_StepTag_Step] FOREIGN KEY ([StepId]) REFERENCES biflow.Step ([StepId]) ON DELETE CASCADE,
	CONSTRAINT [FK_StepTag_Tag] FOREIGN KEY ([TagId]) REFERENCES biflow.Tag ([TagId]) ON DELETE CASCADE
)
