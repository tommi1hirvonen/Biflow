CREATE TABLE [app].[JobStepTagFilter]
(
	[StepId] UNIQUEIDENTIFIER NOT NULL,
	[TagId] UNIQUEIDENTIFIER NOT NULL,
	CONSTRAINT [PK_JobStepTag] PRIMARY KEY CLUSTERED ([StepId], [TagId]),
	CONSTRAINT [FK_JobStepTag_Step] FOREIGN KEY ([StepId]) REFERENCES app.Step ([StepId]) ON DELETE CASCADE,
	CONSTRAINT [FK_JobStepTag_Tag] FOREIGN KEY ([TagId]) REFERENCES app.Tag ([TagId]) ON DELETE CASCADE
)
